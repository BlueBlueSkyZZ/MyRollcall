package entity;

/**
 * 学生实体类
 * @author mzy
 *
 */
public class Student {

	private String id;
	private String team;
	private String name;
	private String stu_id;
	private String answer_num;
	private String team_status;
	private String answer_level;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStu_id() {
		return stu_id;
	}
	public void setStu_id(String stu_id) {
		this.stu_id = stu_id;
	}
	public String getAnswer_num() {
		return answer_num;
	}
	public void setAnswer_num(String answer_num) {
		this.answer_num = answer_num;
	}
	public String getTeam_status() {
		return team_status;
	}
	public void setTeam_status(String team_status) {
		this.team_status = team_status;
	}
	public String getAnswer_level() {
		return answer_level;
	}
	public void setAnswer_level(String answer_level) {
		this.answer_level = answer_level;
	}
	
	
}
